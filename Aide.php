		<p> Tove Marika Jansson est née le 9 août 1914 à Helsinki de Signe Hammarsten (1882-1970), une illustratrice ayant dessiné un bon nombre des timbres finlandais,
			et de Victor Jansson(1886-1958), un sculpteur. Sa mère donnera aussi naissance à Lars (1926-2000) et Per Olov (?-?).</p>

			<p>A l'âge de 16 ans, Tove partira étudier à la Faculté d'Art de Stockholm où elle y restera trois ans. Elle retournera en 1933 dans sa ville natale pour suivre
				des cours à l'Ecole de dessin de la société d'art durant quatre ans. Durant l'année 1938, elle ira étudier à l'Ecole Adrien Holy et à l'Ecole des Beaux Arts à Paris.
				Elle en profitera pour voyager en France, en Italie et en Allemagne.</p>

				<p>Elle remportera durant sa vie de nombreuses récompenses: le prix Nils Holgersson en 1953, le grand prix international Hans Christian Anderson en 1966, trois prix littéraire
					d'Etat en 1963, en 1971 et en 1982 ainsi que le prix Finlande en 1993.</p>

					<p>Elle se mariera avec Tuulikki Pietilä, une artiste graphiste.</p>

					<p>Moumine est le troll inventé par son oncle pour lui faire peur lorsqu'elle descendait au réfrigérateur la nuit. La vallée des Moumines lui a été inspiré par le lieu de vacances
						où ses parents y louaient une cabane de pêcheur tous les étés.</p>

						<p>Son premier Moumine fut Les petits trolls et la grande inondation qu'elle écrivit en 1939 mais qui ne paru qu'en 1945.</p>

						<p>La première parution de Moumine fut en 1941 dans le magazine Garm. En 1958, Moumine fait ses débuts en bandes dessinées avec l'aide de Lars, le frère de Tove, qui
							en prendra la charge en 1961, Tove préférant se consacrer aux romans et la peinture. Lars continuera les BD jusqu'en 1974 et participera au projet de dessins animés.</p>

							<p>Moumine sera aussi adapté au théâtre à plusieurs reprises. En 1949 au Svenska Teater sera joué La comète dans la vallée des Moumines et en 1958 au Lilla Teater,
								Trolli Kulisserna, qui fera une tournée en Suède et en Norvège. Ikka Kuusisto en fera un opéra en 1974.</p>

								<p>Tove Jansson confiera en 1991 plus de deux milles objets et illustrations, en rapport avec la vallée des Moumines, au musée d'art de Tampere.</p>

								<p>Tove meurt le 27 juin 2001 à l'âge de 87 ans. </p>

